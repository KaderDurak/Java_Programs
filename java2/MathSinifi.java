public class MathSinifi {
    public static void main(String[] args) {
        // Math sınıfı metotları
        // Math.ceil(x)  => bir büyük tam sayıya yuvarlama yapar.
        // Math.cos(x)   => cos değerini alır.
        // Math.sin(x)   => sin değerini alır.
        // Math.floor(x) => küçük tamsayıya yuvarlar.
        // Math.max(x,y) => büyük sayıyı verir.
        // Math.min(x,y) => küçük sayıyı verir.
        // Math.pow(3,5) => 3^5 üs alır.
        // Math.random() => 1 ve 0 arasında rastgele değerler alır.
        // Math.round(x) => yakın olan tam sayıyı verir.
        // Math.abs(x)   => mutlak değer


        System.out.println(Math.ceil(1.6));
        System.out.println(Math.cos(30));
        System.out.println(Math.floor(1.9));
        System.out.println(Math.pow(5,9));


    }
}
